export default function Sidebar() {
  const sidebarOpen = useUiStore((s) => s.sidebarOpen)
  const toggleSidebar = useUiStore((s) => s.toggleSidebar)
  const project = useProjectStore((s) => s.project)
  const currentPhase = project?.phase_progress || 0
  const location = useLocation()

  return (
    <aside className={`fixed left-0 top-0 h-full bg-surface border-r border-border z-30 transition-all duration-300 ${sidebarOpen ? 'w-64' : 'w-16'}`}>
      {/* Logo / Brand */}
      <div className="h-16 flex items-center px-4 border-b border-border">
        {sidebarOpen ? (
          <h1 className="text-lg font-bold text-brand-700 truncate">Logo Explorer</h1>
        ) : (
          <span className="text-xl font-bold text-brand-700 mx-auto">L</span>
        )}
      </div>

      {/* Toggle */}
      <button
        onClick={toggleSidebar}
        className="absolute -right-3 top-20 w-6 h-6 bg-surface border border-border rounded-full flex items-center justify-center text-gray-400 hover:text-gray-600 hover:bg-gray-50 transition-colors"
      >
        <svg className={`w-3 h-3 transition-transform ${sidebarOpen ? '' : 'rotate-180'}`} fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
          <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 19.5L8.25 12l7.5-7.5" />
        </svg>
      </button>

      {/* Phase Navigation */}
      <nav className="mt-4 px-2 space-y-1">
        {PHASES.map((phase) => {
          const isActive = location.pathname === phase.path
          const isCompleted = currentPhase > phase.id
          const isAccessible = currentPhase >= phase.id || phase.id === 1

          return (
            <NavLink
              key={phase.id}
              to={isAccessible ? phase.path : '#'}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-brand-50 text-brand-700'
                  : isCompleted
                  ? 'text-gray-700 hover:bg-gray-50'
                  : isAccessible
                  ? 'text-gray-500 hover:bg-gray-50'
                  : 'text-gray-300 cursor-not-allowed'
              }`}
              onClick={(e) => !isAccessible && e.preventDefault()}
            >
              <span className={`flex-shrink-0 ${isCompleted ? 'text-success' : ''}`}>
                {isCompleted ? (
                  <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                    <path strokeLinecap="round" strokeLinejoin="round" d="M9 12.75L11.25 15 15 9.75M21 12a9 9 0 11-18 0 9 9 0 0118 0z" />
                  </svg>
                ) : (
                  phaseIcons[phase.id]
                )}
              </span>
              {sidebarOpen && (
                <div className="min-w-0">
                  <div className="truncate">{phase.name}</div>
                  <div className="text-xs text-gray-400 truncate">{phase.description}</div>
                </div>
              )}
            </NavLink>
          )
        })}
      </nav>
    </aside>
  )
}
